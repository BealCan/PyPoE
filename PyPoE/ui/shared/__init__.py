class SharedMainWindow(QMainWindow):
    """
    Shared Window class for use in sub applications to be launched from the
    Launchpad.
    """

    name = ''

    def closeEvent(self, *args, **kwargs):
        p = self.parent()
        if p is None:
            return

        p.child_closed.emit(self)

